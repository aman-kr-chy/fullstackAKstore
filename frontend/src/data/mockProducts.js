export const generateProducts = () => {
  let allProducts = [];
  Object.keys(categories).forEach((category) => {
    const { images, prefixes, items } = categories[category];
    for (let i = 0; i < 50; i++) {
      const prefix = prefixes[i % prefixes.length];
      const item = items[i % items.length];
      const image = images[i % images.length];
      allProducts.push({
        _id: `${category}-${i}`,
        name: `${prefix} ${item}`,
        category,
        price: Math.floor(Math.random() * (999 - 19 + 1)) + 19,
        rating: (Math.random() * (5 - 3.5) + 3.5).toFixed(1),
        reviews: Math.floor(Math.random() * (500 - 10 + 1)) + 10,
        image,
        description: `High quality ${item.toLowerCase()} perfect for your needs. Features premium materials and excellent build quality.`,
      });
    }
  });
  return allProducts;
};

export const allProducts = generateProducts();
